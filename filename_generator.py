def generate_file_name_tags(folderType, groupType, subGroupType):
    # Zmiana folderType na indeks w liście kluczy w second_level
    folder_index = list(second_level.keys()).index(folderType)
    f = chr(folder_index + 65)  # Przekształcamy indeks na literę (A, B, C,...)

    # Znalezienie indeksu dla groupType w danym folderType
    group_index = list(second_level[folderType].keys()).index(groupType)
    s = chr(group_index + 65)  # Przekształcamy indeks na literę (A, B, C,...)

    # Znalezienie indeksu dla subGroupType w danej grupie groupType
    sub_group_index = list(second_level[folderType][groupType].keys()).index(subGroupType)
    t = chr(sub_group_index + 65)  # Przekształcamy indeks na literę (A, B, C,...)

    return f, s, t


def get_group(folderType):
    # Pobieramy grupy dla danego folderType
    return list(second_level.get(folderType, {}).keys())


def get_subgroup(folderType, groupType):
    # Pobieramy podgrupy dla danego groupType w folderze folderType
    return list(second_level.get(folderType, {}).get(groupType, {}).keys())

def get_int_list(folderType, groupType, subGroupType):
    # Sprawdza, czy folderType istnieje w second_level
    if folderType in second_level:
        # Sprawdza, czy groupType istnieje w folderType
        if groupType in second_level[folderType]:
            # Sprawdza, czy subGroupType istnieje w groupType
            if subGroupType in second_level[folderType][groupType]:
                # Zwraca listę intów dla danego subGroupType
                return second_level[folderType][groupType][subGroupType]
            else:
                print(f"subGroupType '{subGroupType}' nie istnieje w groupType '{groupType}'")
        else:
            print(f"groupType '{groupType}' nie istnieje w folderType '{folderType}'")
    else:
        print(f"folderType '{folderType}' nie istnieje w second_level")
    return None

